class Product {
  final String name;
  final String brand;
  final double cost;
  final String description;
  final bool isNew;
  final double rating;
  final String category;
  final String picturePath;

  Product({
    required this.name,
    required this.brand,
    required this.cost,
    required this.description,
    required this.isNew,
    required this.rating,
    required this.category,
    required this.picturePath,
  });
}
